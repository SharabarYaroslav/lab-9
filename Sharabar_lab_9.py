"""
Напишіть програму, яка реалізує класичний алгоритм сортування рядків
двовимірного масиву методом обміну. Розмірність масиву та всі елементи
вводяться з клавіатури.
"""

# Введення розмірів масиву
n = int(input("Введіть кількість стовпців: "))
m = int(input("Введіть кількість рядків: "))

# Введення елементів масиву
array = []
for i in range(m):
    row = list(map(int, input(f"Введіть елементи для рядка {i+1}, розділені пробілом: ").split()))
    if len(row) != n:
        print(f"Кількість введених елементів не відповідає {n}. Повторіть введення для цього рядка.")
        row = list(map(int, input(f"Введіть елементи для рядка {i+1}, розділені пробілом: ").split()))
    array.append(row)

print("Двовимірний масив з введеними значеннями:")
for row in array:
    print(row)

# Реалізація сортування методом обміну (бульбашкове сортування)
for i in range(m):
    for j in range(n - 1):
        for k in range(n - j - 1):
            if array[i][k] > array[i][k + 1]:
                array[i][k], array[i][k + 1] = array[i][k + 1], array[i][k]

print("Упорядкований масив по рядках:")
for row in array:
    print(row)
